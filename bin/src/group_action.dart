// lib/src/group_action.dart
//
// CSIDH class-group action evaluation.
//
// This is the core of the CSIDH protocol.  Given:
//   • a Montgomery coefficient A  (representing the curve E_A), and
//   • a secret key  e = (e₁, …, e₇₄)  with eᵢ ∈ [−m, m],
//
// the group action  [e] ★ E_A  is computed by applying a sequence of
// ℓᵢ-isogenies:
//
//   E_A  →ˡ¹^|e₁|→  …  →ˡ⁷⁴^|e₇₄|→  E_A′
//
// Positive eᵢ apply an isogeny in the "forward" direction (using a kernel
// point of order ℓᵢ found on E_A(F_p)), negative eᵢ apply the "backward"
// direction (kernel on the quadratic twist  −E_A, equivalent to negating A).
//
// This file implements Algorithm 1 of [CLM+18] — the variable-time version.
//
// References:
//   [CLM+18]  Castryck et al., eprint.iacr.org/2018/383, Algorithm 1.
//   [MR18]    Meyer & Reith, eprint.iacr.org/2018/782 §2 — cleaner
//             presentation of the same algorithm.

import 'dart:math';
import '../src/field.dart';
import '../src/isogeny.dart';
import 'montgomery.dart';
import 'params.dart';

/// Evaluates the CSIDH class-group action  e ★ A.
class GroupAction {
  GroupAction._(); // non-instantiable

  // ── Public entry point ───────────────────────────────────────────────────

  /// Compute  e ★ A  — the group action of secret key `e` on curve `a`.
  ///
  /// Parameters:
  ///   [a]  Montgomery coefficient of the input curve  E_A.
  ///        Pass [CsidhParams.A0] (= 0) for the base curve.
  ///   [e]  Secret key vector  (e₁, …, eₙ),  eᵢ ∈ [−m, m].
  ///        Must have length [CsidhParams.n] = 74.
  ///
  /// Returns the Montgomery coefficient A′ of the image curve E_A′.
  ///
  /// Algorithm: variable-time, matching csidh.c `action()`.
  ///
  /// [ref: action() in Castryck et al. reference C, csidh.c]
  static BigInt apply(BigInt a, List<int> e) {
    assert(e.length == CsidhParams.n, 'key must have ${CsidhParams.n} components');

    // Working copy of the curve coefficient — updated after each isogeny.
    BigInt curA = a;

    // Remaining exponent counters: split positive and negative parts.
    // ePos[i] = max(0,  eᵢ)  — number of forward  ℓᵢ-isogenies to apply.
    // eNeg[i] = max(0, −eᵢ)  — number of backward ℓᵢ-isogenies to apply.
    final ePos = List<int>.generate(CsidhParams.n, (i) => e[i] > 0 ? e[i] : 0);
    final eNeg = List<int>.generate(CsidhParams.n, (i) => e[i] < 0 ? -e[i] : 0);

    // Total isogenies left to apply in each direction.
    int posLeft = ePos.fold(0, (s, v) => s + v);
    int negLeft = eNeg.fold(0, (s, v) => s + v);

    // The outer loop runs until every exponent has been exhausted.
    // Each iteration: pick a random x, check which ℓᵢ-isogenies can be
    // applied (kernel point of order ℓᵢ found), and apply them.
    //
    // [ref: outer while-loop of action() in csidh.c]
    while (posLeft > 0 || negLeft > 0) {
      // -- Sample a random x on the curve or its twist ----------------------
      //
      // If posLeft > 0 we want a point on E_A (forward direction).
      // If negLeft > 0 we want a point on the twist (backward direction).
      // We handle both in the same pass by tracking the Legendre symbol.

      // For the backward direction we negate A, giving the twist curve
      // E_{−A} : y² = x³ − Ax² + x.  Points on E_{−A}(F_p) are exactly
      // the points on E_A that are defined over F_{p²} but not F_p
      // (i.e. x gives a non-residue RHS on E_A).
      //
      // [ref: [CLM+18] §2, "quadratic twist trick"]

      final rng = Random.secure();

      BigInt x;
      int leg; // Legendre symbol of rhs on E_A

      // Sample until we find an x that is useful for at least one direction
      // where we still have isogenies to apply.
      do {
        x = _randomFp(rng);
        leg = Montgomery.hasPoint(x, curA)
            ? 1   // QR  → point on E_A
            : -1; // NQR → point on twist

        // Accept only if that direction still has work to do.
      } while ((leg == 1 && posLeft == 0) || (leg == -1 && negLeft == 0));

      // -- Determine working curve A_work -----------------------------------
      //
      // If leg = −1 we flip to the twist: A_work = −A.
      // The isogenies are applied to the twist; at the end we flip back.
      //
      // [ref: csidh.c uses the negation trick transparently]
      final aWork = leg == 1 ? curA : Fp.neg(curA);

      // -- Multiply x by the cofactor 4 to kill the cofactor part -----------
      //
      // #E_A(F_p) = 4 · ℓ₁·…·ℓ₇₄.  Multiplying by 4 gives a point in the
      // prime-order subgroup generated by ℓ₁,…,ℓ₇₄.
      //
      // [ref: [CLM+18] Algorithm 1, "T ← [4]T"]
      XPoint t = XPoint(x, BigInt.one);
      t = Montgomery.xdbl(Montgomery.xdbl(t, aWork), aWork); // [4]x

      // -- Walk through all primes, applying isogenies where possible -------
      BigInt aWalked = aWork;

      for (int i = 0; i < CsidhParams.n; i++) {
        final cnt = leg == 1 ? ePos[i] : eNeg[i];
        if (cnt == 0) continue;           // nothing to do for this prime
        if (t.isInfinity) break;          // exhausted the random point

        final ell = CsidhParams.ells[i];
        final ellBig = BigInt.from(ell);

        // Compute  S = [M/ℓᵢ] · T  where  M = ∏ remaining ℓⱼ.
        // This gives a point of order dividing ℓᵢ  (possibly O).
        //
        // For the variable-time implementation we use the simpler approach
        // from csidh.c: accumulate the "cofactor product" progressively
        // by dividing out already-used primes.  Here we compute the full
        // product for each prime (slower but clearer for Phase 1).
        //
        // [ref: csidh.c, inner loop "T ← [M/ℓ] T"]
        final cofactorExp = _productExcluding(i, leg == 1 ? ePos : eNeg);
        final s = Montgomery.ladder(cofactorExp, t, aWalked);

        if (s.isInfinity) continue; // unlucky — no kernel point here

        // S has order dividing ℓᵢ; check it's not O (order = 1 possible).
        // Apply the isogeny  φ: E_aWalked → E_aWalked′  with kernel ⟨S⟩.
        final xk = s.affine;
        final result = Isogeny.step(aWalked, xk, ell, xq: t.isInfinity ? null : t.affine);

        aWalked = result.imageA;

        // Push T through φ for use in subsequent primes.
        if (result.imageX != null) {
          t = XPoint(result.imageX!, BigInt.one);
        } else {
          t = XPoint.infinity;
        }

        // Decrement counter for this prime.
        if (leg == 1) {
          ePos[i]--;
          posLeft--;
        } else {
          eNeg[i]--;
          negLeft--;
        }
      }

      // If we worked on the twist, flip the coefficient back.
      curA = leg == 1 ? aWalked : Fp.neg(aWalked);
    }

    return curA;
  }

  // ── Helpers ───────────────────────────────────────────────────────────────

  /// Product  ∏ ℓⱼ  for j ≠ `skip`  and where `counts[j] > 0`.
  ///
  /// This is the "remaining cofactor" we multiply T by to isolate an
  /// ℓ_skip-order subgroup for kernel-point extraction.
  static BigInt _productExcluding(int skip, List<int> counts) {
    BigInt prod = BigInt.one;
    for (int j = 0; j < CsidhParams.n; j++) {
      if (j == skip) continue;
      if (counts[j] > 0) {
        prod *= BigInt.from(CsidhParams.ells[j]);
      }
    }
    return prod;
  }

  /// Sample a uniform random element of F_p.
  ///
  /// Uses Dart's [Random.secure] (cryptographic RNG) and rejection sampling
  /// to avoid modular bias.
  ///
  /// [ref: random_fp() implicit in csidh.c, using /dev/urandom]
  static BigInt _randomFp(Random rng) {
    final p = CsidhParams.p;
    final bits = p.bitLength;
    BigInt r;
    do {
      r = _randomBigInt(rng, bits);
    } while (r >= p || r == BigInt.zero);
    return r;
  }

  /// Generate a random BigInt with at most `bits` random bits.
  static BigInt _randomBigInt(Random rng, int bits) {
    final bytes = (bits + 7) ~/ 8;
    BigInt result = BigInt.zero;
    for (int i = 0; i < bytes; i++) {
      result = (result << 8) | BigInt.from(rng.nextInt(256));
    }
    // Mask to exactly `bits` bits to avoid exceeding the target range.
    final mask = (BigInt.one << bits) - BigInt.one;
    return result & mask;
  }
}
