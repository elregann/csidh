// lib/src/isogeny.dart
//
// ℓ-isogeny computation on Montgomery curves via Vélu's formulae.
//
// Given a Montgomery curve  E_A : y²=x³+Ax²+x  and a point K of order ℓ
// (a small odd prime), this file computes:
//
//   1. xISOG — the image curve coefficient A′  (the isogeny codomain),
//   2. xEVAL — the image of an arbitrary point Q under the isogeny φ: E_A → E_A′.
//
// Both routines operate on affine x-coordinates only.
//
// Mathematical background:
//   For an odd-degree isogeny with kernel K = ⟨P⟩, the kernel subgroup is
//     {O, P, 2P, …, ((ℓ−1)/2)·P, −P, −2P, …}
//   We use the "positive half"  T = {P, 2P, …, ((ℓ−1)/2)·P}  (|T| = (ℓ−1)/2).
//   Vélu's formulae express A′ and φ in terms of sums and products over T.
//
// Formulae used (x-coordinate, B=1 Montgomery form, Renes 2017):
//
//   For each tᵢ = x([i]K):
//     t  = (3·tᵢ² + 2A·tᵢ + 1) / tᵢ        -- "t value" for E_A
//     u  = t · tᵢ⁻¹                           -- simplified for B=1
//
//   σ = Σ tᵢ − 1/tᵢ     (sum over T)
//   π = Π tᵢ             (product over T)
//
//   A′ = (A·π⁸ − 6·σ·π⁴) / (π⁸)             -- Renes / Elkies
//
//   φ(Q): x → x · Π(x − tᵢ)² / (... )       -- Vélu push-forward
//
// References:
//   [CLM+18]  Castryck et al., eprint.iacr.org/2018/383, Algorithm 1 & 2.
//   [Vel71]   Vélu, "Isogénies entre courbes elliptiques", C.R. Acad. Sci.
//             Paris (1971).
//   [Ren17]   Renes, "Complete addition formulas for prime order elliptic
//             curves", EUROCRYPT 2016, Appendix D.
//   [MR18]    Meyer & Reith, eprint.iacr.org/2018/782 — Montgomery / Edwards
//             comparison; the x-only A′ formula used here comes from §3.

import 'field.dart';
import '../src/montgomery.dart';

/// Result of an isogeny evaluation: new curve A′ and image point x′.
class IsogenyResult {
  /// Montgomery coefficient of the image curve E_A′.
  final BigInt imageA;

  /// x-coordinate of the image point  φ(Q) on E_A′,
  /// or null if Q was in the kernel (φ(Q) = O).
  final BigInt? imageX;

  const IsogenyResult({required this.imageA, this.imageX});
}

/// Vélu-based ℓ-isogeny computation on Montgomery curves (x-coordinate only).
class Isogeny {
  Isogeny._(); // non-instantiable

  // ── xISOG — image curve coefficient ─────────────────────────────────────

  /// Compute the image curve coefficient  A′  of the codomain of the
  /// ℓ-isogeny with kernel generated by  K  (affine x-coordinate).
  ///
  /// Algorithm (variable-time, matching csidh.c `isog()`):
  ///
  ///   1. Walk the kernel half T = {[1]K, [2]K, …, [ℓ−1)/2]K} by successive
  ///      differential additions (xDBL + xADD chain).
  ///   2. Accumulate σ and π (projective to avoid inversions in the loop).
  ///   3. Recover A′ from the final σ and π.
  ///
  /// Returns the affine coefficient A′ ∈ F_p.
  ///
  /// [ref: isog() first half in csidh.c]
  static BigInt xisog(BigInt a, BigInt xk, int ell) {
    // -- Step 1: generate kernel points [i]K for i = 1 … (ℓ−1)/2 -----------

    // Kernel half size
    final halfLen = (ell - 1) ~/ 2;

    // We track projective kernel points to defer inversions.
    // kPts[i] = projective XPoint of [(i+1)]K,  i = 0 … halfLen−1.
    final kPts = <XPoint>[];

    XPoint cur = XPoint(xk, BigInt.one); // [1]K (affine → projective)
    kPts.add(cur);

    if (halfLen > 1) {
      XPoint dbl = Montgomery.xdbl(cur, a); // [2]K
      if (halfLen > 2) {
        kPts.add(dbl);
      }
      // Walk [3]K, [4]K, … using differential addition
      XPoint prev = cur;
      for (int i = 2; i < halfLen; i++) {
        final next = Montgomery.xadd(dbl, kPts[i - 2], prev);
        prev = dbl;
        dbl = next;
        kPts.add(dbl);
      }
      if (halfLen == 2) kPts.add(dbl);
    }

    // -- Step 2: compute σ and π projectively --------------------------------

    // We accumulate:
    //   σN/σD  =  Σᵢ (tᵢ·xᵢ − 1) / xᵢ   (numerator/denominator projective)
    //   πN/πD  =  Π xᵢ
    //
    // where for each kernel point (Xᵢ : Zᵢ):
    //   xᵢ (affine) = Xᵢ · Zᵢ⁻¹
    //
    // To stay projective we use:
    //   πN = Π Xᵢ,   πD = Π Zᵢ
    //
    // For σ we convert each xᵢ to affine (one inversion per kernel point)
    // to keep the code readable.  The limb-based layer will batch-invert.

    BigInt piNum = BigInt.one;
    BigInt piDen = BigInt.one;
    BigInt sigma = BigInt.zero;

    for (final pt in kPts) {
      // affine xᵢ
      final xi = pt.isInfinity ? BigInt.zero : pt.affine;

      // πN = Π Xᵢ,   πD = Π Zᵢ  (projective product)
      piNum = Fp.mul(piNum, pt.x);
      piDen = Fp.mul(piDen, pt.z);

      // σ += xᵢ − 1/xᵢ  (Vélu sum, affine)
      // tᵢ term for Montgomery B=1: the "t value" simplifies to xᵢ − 1/xᵢ
      // Reference: [Ren17] Appendix D, eq. (D.2), B=1 special case.
      if (xi != BigInt.zero) {
        sigma = Fp.add(sigma, Fp.sub(xi, Fp.inv(xi)));
      }
    }

    // -- Step 3: recover A′ --------------------------------------------------

    // π = πN · πD⁻¹  (affine)
    final pi = Fp.mul(piNum, Fp.inv(piDen));

    // pi^4  and  pi^8
    final pi4 = Fp.sq(Fp.sq(pi));
    final pi8 = Fp.sq(pi4);

    // A′ = (A · π⁸ − 6 · σ · π⁴) / π⁸
    //
    // [ref: [CLM+18] Appendix A, "image curve formula";
    //        [MR18] §3, eq. for A_new in Montgomery form.]
    final num = Fp.sub(
      Fp.mul(a, pi8),
      Fp.mul(BigInt.from(6), Fp.mul(sigma, pi4)),
    );
    return Fp.mul(num, Fp.inv(pi8));
  }

  // ── xEVAL — push-forward of a point through the isogeny ─────────────────

  /// Evaluate the ℓ-isogeny (kernel ⟨K⟩) at an arbitrary point Q,
  /// returning the x-coordinate of  φ(Q)  on the image curve.
  ///
  /// Returns null if Q is in the kernel (φ(Q) = O).
  ///
  /// Formula (Vélu push-forward, x-only, B=1):
  ///
  ///   x′ = x · Π ( (x·xᵢ − 1)² / (x − xᵢ)² )   over i = 1 … (ℓ−1)/2
  ///
  /// This is the "x-only Vélu" formula; it skips y-coordinates entirely.
  ///
  /// [ref: isog() second half in csidh.c]
  /// [ref: [Vel71]; [CLM+18] Algorithm 2, step "evaluate isogeny at T"]
  static BigInt? xeval(BigInt a, BigInt xk, int ell, BigInt xq) {
    final halfLen = (ell - 1) ~/ 2;

    // Rebuild kernel half T exactly as in xisog.
    // (In a production implementation both xisog and xeval would share a
    //  single kernel-walk pass; separated here for clarity.)
    final kXs = <BigInt>[];
    XPoint cur = XPoint(xk, BigInt.one);
    kXs.add(cur.affine);

    if (halfLen > 1) {
      XPoint dbl = Montgomery.xdbl(cur, a);
      if (halfLen == 2) {
        kXs.add(dbl.affine);
      } else {
        kXs.add(dbl.affine);
        XPoint prev = cur;
        for (int i = 2; i < halfLen; i++) {
          final next = Montgomery.xadd(dbl, XPoint(kXs[i - 2], BigInt.one), prev);
          prev = dbl;
          dbl = next;
          kXs.add(dbl.affine);
        }
      }
    }

    // Vélu product accumulation
    // x′ = x · Π ((x·xᵢ−1)²/(x−xᵢ)²)
    BigInt xNew = xq;

    for (final xi in kXs) {
      final num = Fp.sq(Fp.sub(Fp.mul(xq, xi), BigInt.one));
      final den = Fp.sq(Fp.sub(xq, xi));

      if (den == BigInt.zero) {
        // Q is in the kernel — φ(Q) = O
        return null;
      }

      xNew = Fp.mul(xNew, Fp.mul(num, Fp.inv(den)));
    }

    return xNew;
  }

  // ── Combined: one isogeny step in the group-action walk ──────────────────

  /// Perform one isogeny step of the CSIDH group action:
  ///
  ///   Given curve E_A, kernel generator K (x-coordinate), and an optional
  ///   "carry" point Q to push through, return:
  ///     • the image curve A′, and
  ///     • the image x′ = φ(Q)  (null if Q was in the kernel or Q was null).
  ///
  /// This is the atomic building block called from [GroupAction.apply].
  ///
  /// [ref: combined call of isog() in the action loop of csidh.c]
  static IsogenyResult step(
    BigInt a,
    BigInt xk,
    int ell, {
    BigInt? xq,
  }) {
    final aNew = xisog(a, xk, ell);
    final xNew = xq != null ? xeval(a, xk, ell, xq) : null;
    return IsogenyResult(imageA: aNew, imageX: xNew);
  }
}
